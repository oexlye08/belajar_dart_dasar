/// Operator perbandingan digunakan untuk membandingkan dua buah nilai
void main() {
  // Operator perbandingan
  int a = 10;
  int b = 5;
  bool c = a > b;
  print(c);

  bool d = a < b;
  print(d);

  bool e = a >= b;
  print(e);

  bool f = a <= b;
  print(f);

  bool g = a == b;
  print(g);

  bool h = a != b;
  print(h);
}
