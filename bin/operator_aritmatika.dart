/// Operator aritmatika adalah operator yang digunakan untuk melakukan operasi matematika
void main() {
  // Penjumlahan
  int a = 10;
  int b = 5;
  int c = a + b;
  print(c);

  // Pengurangan
  int d = a - b;
  print(d);

  // Perkalian
  int e = a * b;
  print(e);

  // Pembagian dengan hasil (Double Division)
  double f = a / b;
  print(f);

  // Pembagian dengan pembulatan ke bawah (Integer Division)
  int f2 = a ~/ b;
  print(f2);

  // Modulus
  int g = a % b;
  print(g);

  // Increment
  int h = 10;
  h++;
  print(h);

  // Decrement
  int i = 10;
  i--;
  print(i);
}
