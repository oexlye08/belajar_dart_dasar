/// Ternary operator is a conditional operator that is used to evaluate a condition and return a value based on the result of the condition.
/// It is also known as conditional operator.
/// It is a shortcut for if-else statement.
/// It is a one line if-else statement.

void main() {
  var nilai = 80;
  var ucapan = nilai >= 80 ? "Selamat anda lulus" : "Maaf anda tidak lulus";

  print(ucapan);
}
